    public AnggotaDashboard() {
        User anggota = Session.getCurrentUser();
        if (anggota == null) {
            JOptionPane.showMessageDialog(null, "Akses ditolak");
            new Login().setVisible(true);
            this.dispose();
            return;
        }
        
        initComponents();

        this.setResizable(false);
        this.setLocationRelativeTo(null);

        formSearch = new Form_Search_Anggota();
        formRiwayat = new Form_Riwayat_Anggota();

        menu.addEventMenuSelected(new EventSelected() {
            @Override
            public void selected(int index) {
                if (index == 0) {
                    setForm(formSearch);
                } else if (index == 1) {
                    setForm(formRiwayat);
                }
            }
        });

        setForm(new Form_Search_Anggota());

    }

    private void setForm(JComponent com) {
        mainPanel.removeAll();
        mainPanel.add(com);
        mainPanel.repaint();
        mainPanel.revalidate();
    }

